package SpringDIwithSetterMethod;

public class Engine {

	private int engId;
	private String engName;
	
	public int getEngId() {
		return engId;
	}
	public void setEngId(int engId) {
		this.engId = engId;
	}
	public String getEngName() {
		return engName;
	}
	public void setEngName(String engName) {
		this.engName = engName;
	}
	
	
	
	
}
